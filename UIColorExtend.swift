import Foundation

extension UIColor {
    
    class var SRGreen : UIColor {
        return UIColor(red: 68/255, green: 147/255, blue: 96/255, alpha: 1)
    }
    
    class var SRRed : UIColor {
        return UIColor(red: 210/255, green: 14/255, blue: 21/255, alpha: 1)
    }
    
    class var SRGray : UIColor {
        return UIColor(red: 91/255, green: 93/255, blue: 92/255, alpha: 1)
    }
    
    class var SRLightGreen : UIColor {
        return UIColor(red: 72/255, green: 170/255, blue: 62/255, alpha: 1)
    }
    
    class var SRStatusRefused : UIColor {
        return UIColor(red: 178/255, green: 0/255, blue: 18/255, alpha: 1)
    }
    
    class var SRStatusAccepted : UIColor {
        return UIColor(red: 0/255, green: 128/255, blue: 0/255, alpha: 1)
    }
    
    class var SRStatusWaiting : UIColor {
        return UIColor(red: 255/255, green: 128/255, blue: 0/255, alpha: 1)
    }
    
}